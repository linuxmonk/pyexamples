#!/usr/bin/env python3

"""
Booleans can only be 
 True
 False
"""

a_boolean = True
not_true = False
print(a_boolean)
print(not_true)

"""
Operators are 
 == equals
 >  greater
 >= greater than or equals
 < less
 <= less than or equals
 != not equals
"""
print("1 == 2: {}".format(1 == 2))

"""
Boolean logic operators are
  and 
  or
  not
"""
print("2 > 1 and 2 >= 2: {}".format(2 > 1 and 2 >= 2))

""" Order of operations can be controlled using parenthesis """
